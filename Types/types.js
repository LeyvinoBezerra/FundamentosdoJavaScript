const v1 = 5;
const v2 = 3.65;
const v3 = true;
const v4 = "abc";
const v5 = 'abc';

console.log(typeof(v1)); // -> number
console.log(typeof(v2)); // -> number
console.log(typeof(v3)); // -> boolean
console.log(typeof(v4)); // -> string
console.log(typeof(v5)); // -> string

// Em JavaScript existe apenas um tipo de variável para números (Number),
// não há distinção entre inteiros e pontos flutuantes.

// Em JavaScript, para definir strings, tanto aspas simples (' ') 
// quanto aspas duplas (" ") funcionam da mesma forma.