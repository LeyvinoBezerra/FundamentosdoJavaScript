var v1 = Math.ceil(3.5);
console.log(v1); 
// Math.ceil() arredonda o número para cima (3.5 vira 4)

var v2 = Math.floor(3.5);
console.log(v2); 
// Math.floor() arredonda o número para baixo (3.5 vira 3)

var v3 = Math.round(3.5);
console.log(v3); 
// Math.round() arredonda para o inteiro mais próximo (3.5 vira 4)

var v4 = Math.abs(-15);
console.log(v4); 
// Math.abs() retorna o valor absoluto, ou seja, remove o sinal negativo (-15 vira 15)

var v5 = Math.sqrt(64);
console.log(v5); 
// Math.sqrt() calcula a raiz quadrada do número (√64 = 8)

var v6 = Math.pow(3, 2);
console.log(v6); 
// Math.pow() eleva o primeiro número à potência do segundo (3² = 9)

var v7 = Math.max(3, 5, 10, 6, 1);
console.log(v7); 
// Math.max() retorna o maior valor da lista (nesse caso, 10)

var v8 = Math.min(3, 5, 10, 6, 1);
console.log(v8); 
// Math.min() retorna o menor valor da lista (nesse caso, 1)