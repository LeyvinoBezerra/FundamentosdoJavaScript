console.log(5 < 3); 
// Operador "<" (menor que) → verifica se 5 é menor que 3 → false

console.log(5 > 3); 
// Operador ">" (maior que) → verifica se 5 é maior que 3 → true

console.log(5 <= 3); 
// Operador "<=" (menor ou igual a) → verifica se 5 é menor ou igual a 3 → false

console.log(5 <= 5); 
// Operador "<=" (menor ou igual a) → verifica se 5 é menor ou igual a 5 → true

console.log(5 >= 3); 
// Operador ">=" (maior ou igual a) → verifica se 5 é maior ou igual a 3 → true

console.log(5 >= 5); 
// Operador ">=" (maior ou igual a) → verifica se 5 é maior ou igual a 5 → true

console.log(5 == 3); 
// Operador "==" (igual a) → verifica se 5 é igual a 3 → false