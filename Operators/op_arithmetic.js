console.log(5 + 3); 
// Operador "+" (adição) → soma os valores → resultado: 8

console.log(5 - 3); 
// Operador "-" (subtração) → subtrai o segundo valor do primeiro → resultado: 2

console.log(5 * 3); 
// Operador "*" (multiplicação) → multiplica os valores → resultado: 15

console.log(5 / 3); 
// Operador "/" (divisão) → divide o primeiro valor pelo segundo → resultado: 1.666...

console.log(5 % 3); 
// Operador "%" (módulo ou resto) → retorna o resto da divisão → resultado: 2