// JavaScript é case-sensitive, ou seja, diferencia letras maiúsculas e minúsculas.
// Exemplo: "nome" e "Nome" são variáveis diferentes.
// A indentação do código não altera o funcionamento do programa,
// mas é importante para manter a legibilidade e organização.
// O ponto e vírgula é opcional em alguns casos, pois o JavaScript
// consegue interpretar o fim da instrução automaticamente.
// Ainda assim, usar ponto e vírgula é considerado uma boa prática
// para evitar ambiguidades em códigos mais complexos.

let nome = "Leyvino" // sem ponto e vírgula, funciona
let idade = 25;      // com ponto e vírgula, também funciona

console.log(nome)
console.log(idade);